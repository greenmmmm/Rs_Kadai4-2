package com.example.demo.domain;

public class Okashi {
	private String id;
	private String name;
	private String kal;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getKal() {
		return kal;
	}

	public void setKal(String kal) {
		this.kal = kal;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
